package com.ziyun.academic.entity;

public class EduCourse {
    private String outid;

    private String name;

    private String className;

    private String courseNo;

    private String courseSqe;

    private String courseName;

    private String teacher;

    private String pitchLevel;

    private String schoolroom;

    private String isRebuild;

    private String campus;

    private String alternativeNo;

    private String mayDelete;

    public String getOutid() {
        return outid;
    }

    public void setOutid(String outid) {
        this.outid = outid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getCourseNo() {
        return courseNo;
    }

    public void setCourseNo(String courseNo) {
        this.courseNo = courseNo;
    }

    public String getCourseSqe() {
        return courseSqe;
    }

    public void setCourseSqe(String courseSqe) {
        this.courseSqe = courseSqe;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public String getPitchLevel() {
        return pitchLevel;
    }

    public void setPitchLevel(String pitchLevel) {
        this.pitchLevel = pitchLevel;
    }

    public String getSchoolroom() {
        return schoolroom;
    }

    public void setSchoolroom(String schoolroom) {
        this.schoolroom = schoolroom;
    }

    public String getIsRebuild() {
        return isRebuild;
    }

    public void setIsRebuild(String isRebuild) {
        this.isRebuild = isRebuild;
    }

    public String getCampus() {
        return campus;
    }

    public void setCampus(String campus) {
        this.campus = campus;
    }

    public String getAlternativeNo() {
        return alternativeNo;
    }

    public void setAlternativeNo(String alternativeNo) {
        this.alternativeNo = alternativeNo;
    }

    public String getMayDelete() {
        return mayDelete;
    }

    public void setMayDelete(String mayDelete) {
        this.mayDelete = mayDelete;
    }
}